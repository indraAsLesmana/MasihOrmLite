public class MainMenuModel {

    String name;
    int resourceDrawableSelected;
    int resourceDrawableUnselected;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getResourceDrawableSelected() {
        return resourceDrawableSelected;
    }

    public void setResourceDrawableSelected(int resourceDrawableSelected) {
        this.resourceDrawableSelected = resourceDrawableSelected;
    }

    public int getResourceDrawableUnselected() {
        return resourceDrawableUnselected;
    }

    public void setResourceDrawableUnselected(int resourceDrawableUnselected) {
        this.resourceDrawableUnselected = resourceDrawableUnselected;
    }
}
